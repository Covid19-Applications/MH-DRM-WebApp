relief and more than fifty medical, Army engineering and other rescue operation teams. In 2014, the
Indian Navy was the first to arrive in the Madives to provide fresh drinking water to more than
150,000 of its citizens facing an acute supply crisis. More recently, in response to Cyclone More
(2017), India was the first to respond to the devastating floods in Sri Lanka and Bangladesh. As
reflected in relief provided to Pakistan in 2005 and 2010, Indian support transcends political
considerations and is driven by a deep humanitarian drive. 
                       
