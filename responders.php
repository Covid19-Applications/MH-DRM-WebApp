When the forces of nature unleash their fury on South Asia, the Indian government and military
forces have played a critical role in supporting neighbouring countries in relief operations. After the
2004 tsunami, India deployed 14 Navy vessels, nearly 1,000 military personnel and several dozen
helicopters and airplanes to Sri Lanka. In 2007, in the aftermath of cyclone Sidr, India was one of
the few countries allowed to provide relief to Myanmar and provided critical rice supplies to address
food emergencies there. In 2015, less than six hours after Nepal was rattled by a tragic earthquake
taking almost 9,000 lives, the Indian Air Force flew in National Disaster Response Force (NDRF)
